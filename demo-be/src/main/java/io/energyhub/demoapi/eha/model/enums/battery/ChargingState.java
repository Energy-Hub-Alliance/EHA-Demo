package io.energyhub.demoapi.eha.model.enums.battery;


public enum ChargingState {

    /**
     * The state is in charging.
     */
    CHARGING,

    /**
     * The state is in discharging.
     */
    DISCHARGING,

    /**
     * Idle state.
     */
    IDLE,

    /**
     * The state is in fault.
     */
    FAULT
}